def get_knowledge(agent: str) -> list[str]:
    """获取指定Agent的知识库文本"""
    return KNOWLEDGE_BASES.get(agent, [])

def get_knowledge_context(agent: str, topic: str, max_items: int = 3) -> str:
    """根据话题检索相关知识片段（简单关键词匹配）"""
    items = KNOWLEDGE_BASES.get(agent, [])
    # 简单匹配
    scored = []
    topic_words = set(topic)
    for item in items:
        score = sum(1 for c in topic_words if c in item)
        if score > 0:
            scored.append((score, item))
    scored.sort(key=lambda x: x[0], reverse=True)

    selected = [item for _, item in scored[:max_items]]
    if not selected:
        selected = items[:max_items]  # fallback

    return "\n".join(f"> {s}" for s in selected)

def save_knowledge_bases():
    """保存知识库到文件"""
    kb_dir = Path("E:/debate-arena/knowledge")
    kb_dir.mkdir(parents=True, exist_ok=True)
    for agent, texts in KNOWLEDGE_BASES.items():
        path = kb_dir / f"{agent}.txt"
        path.write_text("\n\n---\n\n".join(texts), encoding='utf-8')
        print(f"  {agent}: {len(texts)} 条")

if __name__ == "__main__":
    save_knowledge_bases()
    print("知识库已保存")
